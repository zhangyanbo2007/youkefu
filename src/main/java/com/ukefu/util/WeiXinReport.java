package com.ukefu.util;

public class WeiXinReport {
	private String data ;
	private long subs ;
	private long unsubs;

	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public long getSubs() {
		return subs;
	}
	public void setSubs(long subs) {
		this.subs = subs;
	}
	public long getUnsubs() {
		return unsubs;
	}
	public void setUnsubs(long unsubs) {
		this.unsubs = unsubs;
	}	
}
